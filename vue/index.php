          <h4 style="font-weight: bold;">Qu'est ce que le Covid19</h4>
          <p>La maladie à coronavirus 2019 Écouter, ou la ou lea Covid-19 Écouter (acronyme anglais de coronavirus disease 2019), est une maladie infectieuse émergente de type zoonose virale causée par la souche de coronavirus SARS-CoV-2. Les symptômes les plus fréquents sont la fièvre, la toux, la fatigue et la gêne respiratoire. Dans les formes les plus graves, l'apparition d'un syndrome de détresse respiratoire aiguë peut entraîner la mort, notamment chez les personnes plus fragiles du fait de leur âge ou en cas de comorbidités. Une autre complication mortelle est une réponse exacerbée du système immunitaire inné (choc cytokinique).<br/>

          Une perte brutale de l'odorat (anosmie), associée ou non à une perte du goût (agueusie), est une manifestation relativement fréquente et parfois révélatrice de l'infection par le SARS-CoV-2.<br/>

          Il existe un taux important de formes asymptomatiques. La transmission interhumaine se fait surtout via des gouttelettes respiratoires, postillons comme pour la grippe saisonnière, surtout lors de la parole, de la toux et des éternuements ou lorsque le contact d'une surface contaminée est suivi par celui d'une muqueuse du visage (bouche, nez, yeux), ou par aérosolisation2,3. La période d'incubation est en moyenne de 5 à 6 jours, avec des extrêmes pouvant aller de deux à quatorze jours.<br/>

          Une proportion importante de personnes infectées, notamment d'enfants, ne présentent que peu ou pas de symptômes mais peuvent néanmoins transmettre la maladie. De plus, il a été établi que, chez les personnes symptomatiques, la contagiosité peut précéder les symptômes de 2 à 3 jours et qu'elle serait maximale durant les premiers jours de la maladie.
